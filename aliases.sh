function showalias () {
    echo "------------------------------  Shell  ------------------------------"
    echo ""
    echo "modzsh: code ~/zsh-config"
    echo "modzurg: code ~/zurg-cli"
    echo "-------------------------------  NPM  -------------------------------"
    echo "vite: Scaffold new project via ViteJS (https://github.com/vitejs/vite)"
}

# Shell
alias modzsh="code ~/zsh-config"
alias modzurg="code ~/zurg-cli"

# NPM
alias vite="npm init vite"
